
# Задача "Магазин игрушек"
# В маленьком магазине игрушек "Веселые карусели" проходит акция для малышей. У каждого маленького покупателя есть возможность выбрать определенную игрушку со скидкой в зависимости от их возраста. Магазин предлагает следующие возрастные категории и скидки:
# Для детей до 3 лет - 10% скидка
# Для детей от 3 до 6 лет - 20% скидка
# Для детей от 6 до 10 лет - 30% скидка
# Для детей от 10 до 14 лет - 40% скидка
# Для детей старше 14 лет - 50% скидка
# В каждой категории скидка распространяется на все игрушки
# 1 Вариант
x = int(input("Введите возраст ребенка и округлите в меньшую сторону до целого числа: "))
# x = 3
match x:
    case 1|2|3:
        print('Вам положена 10% скидка')
    case 4|5|6:
        print('Вам положена 20% скидка')
    case 7|8|9|10:
        print('Вам положена 30% скидка')
    case 11|12|13|14:
        print('Вам положена 40% скидка')
    case 15 | 16 | 17|18:
        print('Вам положена 50% скидка')
    case _:
        print('Введи возраст ребенка снова')

# 2 Вариант
def get_discount(age):
    match age:
        case age if age < 3:
            return 10
        case age if 3 <= age < 6:
            return 20
        case age if 6 <= age < 10:
            return 30
        case age if 10 <= age < 14:
            return 40
        case age if age >= 14:
            return 50
        case _:
            return 0


def main():
    customers = [
        {"name": "Алена", "age": 1},
        {"name": "Андрей", "age": 4},
        {"name": "Сергей", "age": 8},
        {"name": "Виктор", "age": 13},
        {"name": "Ольга", "age": 17},
    ]

    for customer in customers:
        age = customer["age"]
        discount = get_discount(age)
        print(
            f"Покупатель {customer['name']} ({age} лет) получает скидку {discount}% на игрушки."
        )

if __name__ == "__main__":
    main()

# Задача "Турнир по дартсу"
# В джентльменском клубе "Мастера Дартса" проходит ежемесячный турнир по дартсу.
# Участникам предстоит соревноваться в меткости и точности бросков. Организаторы решили автоматизировать процесс подсчета очков с помощью специальной программы.
# У каждого участника есть возможность совершить 5 бросков в каждом раунде. За каждое попадание в мишень игрок получает определенное количество очков.
# В зависимости от набранных очков после 5 бросков игроки будут распределены по разным категориям.

def get_category(total_score):
    match total_score:
        case score if score < 25:
            return "Новичок"
        case score if 25 <= score < 50:
            return "Любитель"
        case score if 50 <= score < 100:
            return "Профессионал"
        case score if score >= 100:
            return "Мастер"
        case _:
            return "Без категории"


def get_scores():
    scores = []
    for i in range(5):
        while True:
            try:
                score = int(input(f"Введите количество очков за бросок {i+1}: "))
                scores.append(score)
                break
            except ValueError:
                print("Введите корректное число.")
    return scores


def main():
    name = input("Введите имя игрока: ")
    print("Введите количество очков за 5 бросков:")
    scores = get_scores()
    total_score = sum(scores)
    category = get_category(total_score)
    print(
        f"Игрок {name} набрал {total_score} очков и попадает в категорию '{category}'."
    )


if __name__ == "__main__":
    main()
